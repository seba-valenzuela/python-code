# New Line: \n
print("Hear \nme \nNOW!")
print()

# Tab indentation: \t
print("These \tare \twords.")
print()

# Ignore formatting: raw strings
print(r"There are 2 options: \n or \t")
print()

# Index of a string: strings can be treated like lists
name = "seba"
print(name[1])
print(len(name))
print()