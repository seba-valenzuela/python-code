# Modulo Operator allows you to iterate through a loop of numbers
# one number stays constant while the others increment

# the number AFTER the % is the RANGE of the loop
# loop always starts at 0
# The HIGHEST number in this loop will be: RANGE-1

print(0 % 4) # 0
print(1 % 4) # 1
print(2 % 4) # 2
print(3 % 4) # 3
print(4 % 4) # 0
print(5 % 4) # 1
print(6 % 4) # 2
print(7 % 4) # 3
