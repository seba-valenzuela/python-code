# Dictionaries consist of Key:Value pairs
data = {'age': '33', 'name': 'Seba', 'height': '69'}
print()

# Access just the Keys:
print(data.keys())
print()

# Print just the Keys:
print(*data.keys()) # <--- The magic asterix! *
print()

# Print just the Values:
print(*data.values()) # <--- The magic asterix! *
print()