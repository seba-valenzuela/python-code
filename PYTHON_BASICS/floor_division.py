# Floor Division (//) gets rid of the decimal point

print(0//3) # 0
print(1//3) # 0
print(2//3) # 0
print(3//3) # 1
print(4//3) # 1
print(5//3) # 1
print(6//3) # 2
print(7//3) # 2
print(8//3) # 2

